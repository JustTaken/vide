const c = @import("../bind.zig").c;

pub const Result = error {
        NotReady,
        Timeout,
        EventSet,
        EventReset,
        OutOfHostMemory,
        OutOfDeviceMemory,
        InitializationFailed,
        DeviceLost,
        MemoryMapFailed,
        LayerNotPresent,
        ExtensionNotPresent,
        FeatureNotPresent,
        IncompatibleDriver,
        TooManyObjects,
        FormatNotSupported,
        FragmentedPool,
        Unknown,
        InvalidExternalHandle,
        Fragmentation,
        InvalidOpaqueCaptureAddress,
        PipelineCompileRequired,
        SurfaceLostKhr,
        NativeWindowInUseKhr,
        SuboptimalKhr,
        OutOfDateKhr,
        IncompatibleDisplayKhr,
        Incomplete,
        ValidationFailedExt,
        InvalidShaderNv,
        ImageUsageNotSupportedKhr,
        VideoPictureLayoutNotSupportedKhr,
        VideoProfileOperationNotSupportedKhr,
        VideoProfileFormatNotSupportedKhr,
        VideoProfileCodecNotSupportedKhr,
        VideoStdVersionNotSupportedKhr,
        InvalidDrmFormatModifierPlaneLayoutExt,
        NotPermittedKhr,
        FullScreenExclusiveModeLostExt,
        ThreadIdleKhr,
        ThreadDoneKhr,
        OperationDeferredKhr,
        OperationNotDeferredKhr,
        InvalidVideoStdParametersKhr,
        CompressionExhaustedExt,
        IncompatibleShaderBinaryExt,
        OutOfPoolMemoryKhr,
        Else,
};

pub fn check(result: i32) Result!void {
    switch (result) {
            c.VK_SUCCESS => return,
            c.VK_NOT_READY => return Result.NotReady,
            c.VK_TIMEOUT => return Result.Timeout,
            c.VK_EVENT_SET => return Result.EventSet,
            c.VK_EVENT_RESET => return Result.EventReset,
            c.VK_INCOMPLETE => return Result.Incomplete,
            c.VK_ERROR_OUT_OF_HOST_MEMORY => return Result.OutOfHostMemory,
            c.VK_ERROR_OUT_OF_DEVICE_MEMORY => return Result.OutOfDeviceMemory,
            c.VK_ERROR_INITIALIZATION_FAILED => return Result.InitializationFailed,
            c.VK_ERROR_DEVICE_LOST => return Result.DeviceLost,
            c.VK_ERROR_MEMORY_MAP_FAILED => return Result.MemoryMapFailed,
            c.VK_ERROR_LAYER_NOT_PRESENT => return Result.LayerNotPresent,
            c.VK_ERROR_EXTENSION_NOT_PRESENT => return Result.ExtensionNotPresent,
            c.VK_ERROR_FEATURE_NOT_PRESENT => return Result.FeatureNotPresent,
            c.VK_ERROR_INCOMPATIBLE_DRIVER => return Result.IncompatibleDriver,
            c.VK_ERROR_TOO_MANY_OBJECTS => return Result.TooManyObjects,
            c.VK_ERROR_FORMAT_NOT_SUPPORTED => return Result.FormatNotSupported,
            c.VK_ERROR_FRAGMENTED_POOL => return Result.FragmentedPool,
            c.VK_ERROR_UNKNOWN => return Result.Unknown,
            c.VK_ERROR_INVALID_EXTERNAL_HANDLE => return Result.InvalidExternalHandle,
            c.VK_ERROR_FRAGMENTATION => return Result.Fragmentation,
            c.VK_ERROR_INVALID_OPAQUE_CAPTURE_ADDRESS => return Result.InvalidOpaqueCaptureAddress,
            c.VK_PIPELINE_COMPILE_REQUIRED => return Result.PipelineCompileRequired,
            c.VK_ERROR_SURFACE_LOST_KHR => return Result.SurfaceLostKhr,
            c.VK_ERROR_NATIVE_WINDOW_IN_USE_KHR => return Result.NativeWindowInUseKhr,
            c.VK_SUBOPTIMAL_KHR => return Result.SuboptimalKhr,
            c.VK_ERROR_OUT_OF_DATE_KHR => return Result.OutOfDateKhr,
            c.VK_ERROR_INCOMPATIBLE_DISPLAY_KHR => return Result.IncompatibleDisplayKhr,
            c.VK_ERROR_VALIDATION_FAILED_EXT => return Result.ValidationFailedExt,
            c.VK_ERROR_INVALID_SHADER_NV => return Result.InvalidShaderNv,
            c.VK_ERROR_IMAGE_USAGE_NOT_SUPPORTED_KHR => return Result.ImageUsageNotSupportedKhr,
            c.VK_ERROR_VIDEO_PICTURE_LAYOUT_NOT_SUPPORTED_KHR => return Result.VideoPictureLayoutNotSupportedKhr,
            c.VK_ERROR_VIDEO_PROFILE_OPERATION_NOT_SUPPORTED_KHR => return Result.VideoProfileOperationNotSupportedKhr,
            c.VK_ERROR_VIDEO_PROFILE_FORMAT_NOT_SUPPORTED_KHR => return Result.VideoProfileFormatNotSupportedKhr,
            c.VK_ERROR_VIDEO_PROFILE_CODEC_NOT_SUPPORTED_KHR => return Result.VideoProfileCodecNotSupportedKhr,
            c.VK_ERROR_VIDEO_STD_VERSION_NOT_SUPPORTED_KHR => return Result.VideoStdVersionNotSupportedKhr,
            c.VK_ERROR_INVALID_DRM_FORMAT_MODIFIER_PLANE_LAYOUT_EXT => return Result.InvalidDrmFormatModifierPlaneLayoutExt,
            c.VK_ERROR_NOT_PERMITTED_KHR => return Result.NotPermittedKhr,
            c.VK_ERROR_FULL_SCREEN_EXCLUSIVE_MODE_LOST_EXT => return Result.FullScreenExclusiveModeLostExt,
            c.VK_THREAD_IDLE_KHR => return Result.ThreadIdleKhr,
            c.VK_THREAD_DONE_KHR => return Result.ThreadDoneKhr,
            c.VK_OPERATION_DEFERRED_KHR => return Result.OperationDeferredKhr,
            c.VK_OPERATION_NOT_DEFERRED_KHR => return Result.OperationNotDeferredKhr,
            c.VK_ERROR_INVALID_VIDEO_STD_PARAMETERS_KHR => return Result.InvalidVideoStdParametersKhr,
            c.VK_ERROR_COMPRESSION_EXHAUSTED_EXT => return Result.CompressionExhaustedExt,
            c.VK_ERROR_INCOMPATIBLE_SHADER_BINARY_EXT => return Result.IncompatibleShaderBinaryExt,
            c.VK_ERROR_OUT_OF_POOL_MEMORY_KHR => return Result.OutOfPoolMemoryKhr,

            else => return Result.Else,
    }
}
